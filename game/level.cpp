extern char* path;

void loadLevel1(){
    if ( !init() ){
        cout << "Failed to initialize!\n";
    }
    else {
        //load media
        path = "../image/background2.png";
        image = loadSurface(path);

        //apply the image
        SDL_BlitSurface(image, nullptr, screenSurface, nullptr);
        //Update
        SDL_UpdateWindowSurface(window);

        eloop();
    }
    //free resourses and close
    close();
}

